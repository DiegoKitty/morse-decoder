const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
    '**********': " ",
};

function decode(expr) {
    const interval = 10;
    let string = "";

    for (i = 0; i < expr.length; i += interval) {
        let str = expr.substring(i, i + interval);
        str = str.substring(str.indexOf("1")).replace(/10/g, ".").replace(/11/g, "-");
        string += [str].map(e => e = MORSE_TABLE[e]).join("");
    }
    return string;
}

module.exports = {
    decode
}